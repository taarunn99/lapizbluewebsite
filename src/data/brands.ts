/**
 * Get all published brands
 */
export function getAllBrands(): BrandData[] {
  return brands.filter((brand) => brand.isPublished);
}

/**
 * Get a single brand by slug
 */
export function getBrandBySlug(slug: string): BrandData | null {
  const brand = brands.find((b) => b.slug === slug && b.isPublished);
  return brand || null;
}

/**
 * Get all brand slugs for static generation
 */
export function getAllBrandSlugs(): string[] {
  return brands.filter((brand) => brand.isPublished).map((brand) => brand.slug);
}

/**
 * Validate brand data - throws descriptive errors for missing required fields
 */
export function validateBrandData(brand: BrandData): void {
  const required = [
    "slug",
    "name",
    "tagline",
    "seo",
    "logo",
    "heroImage",
    "heroImageAlt",
    "ogImage",
    "description",
  ];

  for (const field of required) {
    if (!brand[field as keyof BrandData]) {
      throw new Error(
        `Brand "${brand.name || brand.slug || "Unknown"}": Missing required field "${field}"`
      );
    }
  }

  // Validate slug format
  if (!/^[a-z0-9-]+$/.test(brand.slug)) {
    throw new Error(
      `Brand "${brand.name}": Slug must be lowercase, hyphenated, and contain no special characters (got: "${brand.slug}")`
    );
  }

  // Validate SEO fields
  if (!brand.seo.metaTitle || !brand.seo.metaDescription) {
    throw new Error(`Brand "${brand.name}": SEO metaTitle and metaDescription are required`);
  }
}

/**
 * Validate all brands on import
 */
brands.forEach((brand) => {
  try {
    validateBrandData(brand);
  } catch (error) {
    console.error("Brand data validation error:", error);
    // Don't throw in production, just log
    if (process.env.NODE_ENV === "development") {
      throw error;
    }
  }
});
